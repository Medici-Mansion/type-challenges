/**
 * intrinsic string manipulation types
 *
 * 문자열 조작을 돕기 위한 타입으로, typescript 컴파일러에 포함되어 있다.  `.d.ts`파일엔 유형 세트가 존재하지 않지만,
 * 컴파일러에서 타입 추론 가능
 *
 */
type MyCapitalize< S extends string> = S extends `${infer F}${infer Rest}` ? `${Uppercase<F>}${Rest}` : S
